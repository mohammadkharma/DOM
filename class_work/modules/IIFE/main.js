// IIFE: (Immediately Invoked Function Expression)
// =============================================== //


// IIFE is a function that doesn't need to be called and executed once.
// As soon as the function is declared, it will get executed.
// The function is anonymous and without a name.

(function () {

    let x = 10;
    let y = 10;

    let result = x + y;
    console.log(result);

})();